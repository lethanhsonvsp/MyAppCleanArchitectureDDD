using MediatR;
using Microsoft.AspNetCore.Mvc;
using MyApp.Application.Commands;
using MyApp.Application.Queries;
using MyApp.Shared.Contracts.Charging;

namespace MyApp.Controllers;

[ApiController]
[Route("api/[controller]")]
public class ChargingController : ControllerBase
{
    private readonly IMediator _mediator;
    private readonly ILogger<ChargingController> _logger;

    public ChargingController(IMediator mediator, ILogger<ChargingController> logger)
    {
        _mediator = mediator;
        _logger = logger;
    }

    // ?????????????????????????????????????????????????????????????
    // GET STATUS
    // ?????????????????????????????????????????????????????????????

    [HttpGet("status")]
    public async Task<IActionResult> GetStatus()
    {
        var result = await _mediator.Send(new GetChargingStatusQuery());

        if (result == null)
            return NotFound();

        return Ok(result);
    }

    // ?????????????????????????????????????????????????????????????
    // GET STATS
    // ?????????????????????????????????????????????????????????????

    [HttpGet("stats")]
    public async Task<IActionResult> GetStats()
    {
        var result = await _mediator.Send(new GetChargingStatsQuery());

        if (result == null)
            return NotFound();

        return Ok(result);
    }

    // ?????????????????????????????????????????????????????????????
    // START CHARGING
    // ?????????????????????????????????????????????????????????????

    [HttpPost("start")]
    public async Task<IActionResult> StartCharging([FromBody] StartChargingRequest request)
    {
        _logger.LogInformation("Starting charging: {Voltage}V, {Current}A",
            request.Voltage_V, request.Current_A);

        var result = await _mediator.Send(new StartChargingCommand(
            request.Voltage_V,
            request.Current_A
        ));

        return Ok(new StartChargingResponse
        {
            Success = result.IsSuccess,
            ErrorMessage = result.ErrorMessage
        });
    }

    // ?????????????????????????????????????????????????????????????
    // STOP CHARGING
    // ?????????????????????????????????????????????????????????????

    [HttpPost("stop")]
    public async Task<IActionResult> StopCharging()
    {
        _logger.LogInformation("Stopping charging");

        var result = await _mediator.Send(new StopChargingCommand());

        return Ok(new StopChargingResponse
        {
            Success = result.IsSuccess,
            ErrorMessage = result.ErrorMessage
        });
    }

    // ?????????????????????????????????????????????????????????????
    // CLEAR FAULTS
    // ?????????????????????????????????????????????????????????????

    [HttpPost("clear-faults")]
    public async Task<IActionResult> ClearFaults()
    {
        _logger.LogInformation("Clearing faults");

        var result = await _mediator.Send(new ClearChargingFaultsCommand());

        return Ok(new ClearFaultsResponse
        {
            Success = result.IsSuccess,
            ErrorMessage = result.ErrorMessage
        });
    }
}